import java.lang.System;
import java.lang.String;

public class Ex04 {
    public static void main(String[] args) {

        int a = 4;
        int b = 2;

        System.out.printf("%d + %d = %d\n", a, b, a + b);
        System.out.printf("%d - %d = %d\n", a, b, a - b);
        System.out.printf("%d * %d = %d\n", a, b, a * b);
        System.out.printf("%d / %d = %d\n", a, b, a / b);
    }
}